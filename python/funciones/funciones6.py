print('Bienvenido a calculadora 1.5')

n1 = float(input('Ingrese su primer numero :'))
n2 = float(input('Ingrese su ultimo numero :'))


def suma(n1,n2):
    return n1+n2
def resta(n1,n2):
    return n1-n2
def multiplicacion(n1,n2):
    return n1*n2
def division(n1,n2):
    return n1/n2

suma(n1 + n2)

resta(n1 - n2)

multiplicacion(n1 * n2)

division(n1 / n2)